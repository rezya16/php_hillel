/*1. Написать функцию для расчета факториала. Функция должна принимать аргумент например n,
которым является целое и не отрицательное число (проверки можете сделать по желанию),
и должна возвращать результат - вычисленный факториал.
Напоминаю что - факториал натурального числа n определяется как произведение всех натуральных чисел от 1 до n включительно*/
function factorial ($i){
    $n=0;
    $s=1;
    while (++$n<=$i){
        $s=$s*$n;
    }
    return $s;
}
echo factorial (8)."<br/>";

/*2. Написать функцию которая разворачивает строку в обратном порядке,
например строка “пример” если ее развернуть обратно будет “ремирп”.
Здесь я хочу напомнить что вы можете работать с строкой как с массивом
Как подсказка которая касается конкретного задания,
вам надо использовать какую то переменную как переменную накопитель.*/

function reverse($str){
    $l=strlen($str);
    $n = 0;
    while ($l--!=0){
        $reverse[$n++] = $str[$l];
    }
    return $reverse;
}
reverse('One1 Two2 Three3');
