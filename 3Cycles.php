/*3. Написать функцию для линейного поиска элемента в массиве.
Линейный поиск, это поиск элемента путем перебора.
Ваша функция должна принимать два аргумента 1) массива в котором вы что то ищите, и 2) значение которое вы ищите.
Функция должна возвращать индекс элемента массива который был найден.*/

function search($array,$desired)
{
    foreach ($array as $key => $value) {
        if ($value == $desired){
            return $key;
        }
    }
}
echo search([0,1,2,3,4,5,6,7],3)."<br/>";